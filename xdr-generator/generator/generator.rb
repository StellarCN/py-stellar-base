    when AST::Typespecs::Bool
      "Boolean.to_json_dict(#{value})"
    when AST::Typespecs::Opaque
      "Opaque.to_json_dict(#{value})"
    when AST::Typespecs::String
      "String.to_json_dict(#{value})"
    else
