            return cls(
                hi_hi=Int64(int.from_bytes(value_bytes[0:8], "big", signed=True)),
                hi_lo=Uint64(int.from_bytes(value_bytes[8:16], "big", signed=False)),
                lo_hi=Uint64(int.from_bytes(value_bytes[16:24], "big", signed=False)),
